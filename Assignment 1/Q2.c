/*Write a program to accept a number and print the number in character, decimal, octal and hex
formats.*/


#include<stdio.h>

int main()

{

int a;

scanf("%d",&a);

printf("the given number in char is:%c \n",a );
printf("print the given number in decimal:%d \n",a);
printf("print the given number in octal:%o \n",a);
printf("print the given number in hexa:%x \n",a);


return 0;
}

